package basic;

public class LearnEscapeSequence {
	
	// Escape Sequence
	
	public static void main(String[] args) {
		
		System.out.println("This is QA class");
		System.out.println("This is \tQA class"); //Using tab
		System.out.println("This is QA class1\b1"); //Backspace 
		System.out.println("This is QA \nclass");   //New line
		System.out.println("This is \'QA\' class"); //Single Quotation
		System.out.println("This is \"QA\" class"); //Double quotation
		System.out.println("This is QA \\class"); // Enter backspace 
	}
	

}
